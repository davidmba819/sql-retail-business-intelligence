-- F3: CUSTOMER PURCHASE BEHAVIOR ANALYSIS
-- ==================================================


-- ==================================================
-- F3.1: AVERAGE PRODUCTS PURCHASED PER CUSTOMER
-- ==================================================

-- Business Question:
-- How many products does the average customer purchase?

SELECT 
	(COUNT(oi.order_item_id) / COUNT(DISTINCT c.customer_unique_id)) AS avg_purchase_items
FROM valid_orders vo
INNER JOIN customers c
ON c.customer_id = vo.customer_id
INNER JOIN order_items oi
ON oi.order_id = vo.order_id;

-- Key Insight:
-- Customers purchased an average of 1.18 products per customer.
-- This suggests that most customers bought a single item,
-- with relatively few customers purchasing multiple products.


-- ==================================================
-- F3.2: AVERAGE CUSTOMER SPEND
-- ==================================================

-- Business Question:
-- How much does the average customer spend?
WITH customer_spending AS (
    SELECT
        c.customer_unique_id,
        SUM(oi.price + oi.freight_value) AS total_customer_spend
    FROM valid_orders vo
    INNER JOIN customers c
        ON c.customer_id = vo.customer_id
    INNER JOIN order_items oi
        ON oi.order_id = vo.order_id
    GROUP BY c.customer_unique_id
)

SELECT
    AVG(total_customer_spend) AS avg_customer_spend
FROM customer_spending;

-- Key Insight:
-- The average customer spent approximately 166.03.
-- This suggests that customer spending is relatively concentrated
-- around low-to-moderate purchase values, with a smaller group of
-- customers contributing significantly higher spending amounts.

-- ==================================================
-- F3.3: ORDER FREQUENCY VS CUSTOMER SPENDING
-- ==================================================

-- Business Question:
-- What is the relationship between order frequency and customer spending?

WITH customer_behavior AS (
    SELECT
        c.customer_unique_id,
        COUNT(DISTINCT vo.order_id) AS total_orders,
        SUM(oi.price + oi.freight_value) AS total_spend
    FROM valid_orders vo
    INNER JOIN customers c
        ON c.customer_id = vo.customer_id
    INNER JOIN order_items oi
        ON oi.order_id = vo.order_id
    GROUP BY c.customer_unique_id
)

SELECT
    customer_unique_id,
    total_orders,
    total_spend
FROM customer_behavior
ORDER BY total_orders DESC, total_spend DESC;

-- Key Insight:
-- Customers who place more orders do not necessarily spend more money.
-- While some high-frequency customers generate high revenue,
-- others place many orders with relatively low spending.
-- This suggests that purchase frequency alone is not a reliable indicator
-- of customer value.


