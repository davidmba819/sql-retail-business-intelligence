-- ==================================================
-- F1.1: CUSTOMER ORDER DISTRIBUTION
-- ==================================================

-- Business Question:
-- How are customers distributed by number of orders?
USE retail_business_db;

WITH customer_order_summary AS(
SELECT
	c.customer_unique_id,
    COUNT(vo.order_id) AS order_per_customer
FROM valid_orders vo
INNER JOIN customers c
ON c.customer_id = vo.customer_id
WHERE c.customer_unique_id IS NOT NULL
	AND vo.order_id IS NOT NULL
GROUP BY c.customer_unique_id
    )
    
SELECT
	COUNT(customer_unique_id) AS customer_order_frequency,
    order_per_customer
FROM customer_order_summary
GROUP BY order_per_customer;

-- Finding:
-- Customer purchasing behavior is heavily concentrated among one-time buyers.
-- 93,074 customers placed only one order, while repeat customers represented a much smaller portion of the customer base.
-- Only a few customers placed more than four orders, with the highest observed customer placing 17 orders.


-- ==================================================
-- F1.2: ONE-TIME VS REPEAT CUSTOMER SEGMENTATION
-- ==================================================

-- Business Question:
-- How many customers are one-time buyers,
-- and how many are repeat buyers?

WITH customer_order_summary AS(
SELECT
	c.customer_unique_id,
    COUNT(vo.order_id) AS order_per_customer
FROM valid_orders vo
INNER JOIN customers c
ON c.customer_id = vo.customer_id
WHERE c.customer_unique_id IS NOT NULL
	AND vo.order_id IS NOT NULL
GROUP BY c.customer_unique_id
	),
    
customer_segmentation AS(
SELECT
	customer_unique_id,
    order_per_customer,
	(
		CASE
			WHEN order_per_customer > 1 THEN 'Repeated'
            ELSE 'One_Time'
		END
        ) AS customer_segmentation
FROM customer_order_summary)

SELECT 
	customer_segmentation,
	COUNT(customer_segmentation) AS total_customers
FROM customer_segmentation
GROUP BY customer_segmentation;

-- Finding:
-- The customer base is dominated by one-time buyers, with 93,074 customers placing only one order.
-- Only 2,994 customers made repeat purchases, indicating a relatively low customer retention rate.
-- The business appears to rely heavily on acquiring new customers rather than generating repeat purchases from existing customers.

-- ==================================================
-- F1.3: HIGH-VALUE CUSTOMERS
-- ==================================================

-- Business Question:
-- Who are the highest-value customers?
SELECT
	c.customer_unique_id,
    SUM(oi.price + oi.freight_value) AS total_revenue_per_customer
FROM valid_orders vo
INNER JOIN customers c
ON c.customer_id = vo.customer_id
INNER JOIN order_items oi
ON oi.order_id = vo.order_id
GROUP BY c.customer_unique_id
ORDER BY total_revenue_per_customer DESC
LIMIT 10;

-- Finding:
-- Customer spending is highly concentrated among a small group of customers.
-- The highest-value customer generated 13,664.08 in total revenue.
-- The top 10 customers each generated more than 4,600 in revenue.
-- These customers represent the business's most valuable customer segment and may be important for retention and loyalty initiatives.


-- ==================================================
-- F1.4: REVENUE CONTRIBUTION BY CUSTOMER SEGMENT
-- ==================================================

-- Business Question:
-- How much revenue is generated by repeat customers versus one-time customers?
WITH customer_order_summary AS(
SELECT
	c.customer_unique_id,
    COUNT(oi.order_id) AS total_order_per_cust,
    SUM(oi.price + oi.freight_value) AS total_rev_per_cust
FROM valid_orders vo
INNER JOIN customers c
ON c.customer_id = vo.customer_id
INNER JOIN order_items oi
ON oi.order_id = vo.order_id
WHERE c.customer_unique_id IS NOT NULL 
	AND oi.order_id IS NOT NULL
GROUP BY c.customer_unique_id
),

customer_segmentation AS(
SELECT
	total_order_per_cust,
	total_rev_per_cust,
	(
		CASE
			WHEN total_order_per_cust > 1 THEN 'Repeated'
            ELSE 'One_Time'
		END
        ) AS customer_segmentation
FROM customer_order_summary
)

SELECT
	customer_segmentation,
	COUNT(customer_segmentation) AS customers_seg_count,
    SUM(total_rev_per_cust) AS total_rev_per_seg
FROM customer_segmentation
GROUP BY customer_segmentation;

-- Finding:
-- One-time customers generated 12.70 million in revenue, while repeat customers generated 3.14 million.
-- Although repeat customers represent a much smaller portion of the customer base, they contributed nearly 20% of total revenue.
-- The business relies heavily on one-time customers for revenue generation, but repeat customers remain a valuable revenue segment.


