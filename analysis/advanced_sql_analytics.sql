-- F2: PRODUCT CONCENTRATION ANALYSIS
-- ==================================================


-- ==================================================
-- F2.1: TOP REVENUE-GENERATING PRODUCT CATEGORIES
-- ==================================================

-- Business Question:
-- Which product categories generated the highest revenue?
SELECT
	p.product_category_name,
    COUNT(oi.order_id) AS order_per_product,
    SUM(oi.price + oi.freight_value) AS total_rev_per_product
FROM order_items oi
LEFT JOIN products p
ON p.product_id = oi.product_id
WHERE oi.order_id IS NOT NULL
	AND p.product_id IS NOT NULL
GROUP BY p.product_category_name
ORDER BY SUM(oi.price + oi.freight_value) DESC
LIMIT 10;



-- ==================================================
-- F2.2: HIGHEST AVERAGE REVENUE PER ORDER BY CATEGORY
-- ==================================================

-- Business Question:
-- Which product categories generate the highest average revenue per order?


WITH product_summary AS(
SELECT
	p.product_category_name,
    COUNT(DISTINCT oi.order_id) AS order_per_product,
    SUM(oi.price + oi.freight_value) AS total_rev_per_product
FROM order_items oi
LEFT JOIN products p
ON p.product_id = oi.product_id
WHERE oi.order_id IS NOT NULL
	AND p.product_id IS NOT NULL
GROUP BY p.product_category_name
)

SELECT
	product_category_name,
    (total_rev_per_product / order_per_product) AS avg_rev_per_category
FROM product_summary
ORDER BY (total_rev_per_product / order_per_product) DESC
LIMIT 10;


