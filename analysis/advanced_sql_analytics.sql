-- F4: ORDER LIFECYCLE ANALYSIS
-- ==================================================

-- ==================================================
-- F4.1: LONGEST ORDER LIFECYCLE STAGE
-- ==================================================

-- Business Question:
-- Which stage of the order lifecycle takes the longest time?

SELECT
    ROUND(AVG(TIMESTAMPDIFF(HOUR,
        order_purchase_timestamp,
        order_approved_at)), 2) AS approval_time_hours,

    ROUND(AVG(TIMESTAMPDIFF(HOUR,
        order_approved_at,
        order_delivered_carrier_date)), 2) AS shipping_time_hours,

    ROUND(AVG(TIMESTAMPDIFF(HOUR,
        order_delivered_carrier_date,
        order_delivered_customer_date)), 2) AS delivery_time_hours

FROM valid_orders
WHERE order_purchase_timestamp IS NOT NULL
    AND order_approved_at IS NOT NULL
    AND order_delivered_carrier_date IS NOT NULL
    AND order_delivered_customer_date IS NOT NULL;
    
-- Key Insight:
-- The final delivery stage (carrier to customer) is the longest
-- stage in the order lifecycle, averaging 223.51 hours.
--
-- This stage accounts for the majority of fulfillment time,
-- indicating that last-mile delivery is the primary operational
-- bottleneck in the order process.


-- ==================================================
-- F4.2: SHIPPING TIME FOR DELAYED VS ON-TIME ORDERS
-- ==================================================

-- Business Question:
-- Do delayed deliveries have significantly longer shipping times?

SELECT
    CASE
        WHEN order_delivered_customer_date > order_estimated_delivery_date
        THEN 'Delayed'
        ELSE 'On Time'
    END AS delivery_status,

    ROUND(
        AVG(
            TIMESTAMPDIFF(
                HOUR,
                order_approved_at,
                order_delivered_carrier_date
            )
        ),
        2
    ) AS avg_shipping_time_hours

FROM valid_orders

WHERE order_approved_at IS NOT NULL
    AND order_delivered_carrier_date IS NOT NULL
    AND order_delivered_customer_date IS NOT NULL
    AND order_estimated_delivery_date IS NOT NULL

GROUP BY delivery_status;

-- Key Insight:
-- Delayed orders spent significantly longer in the shipping stage
-- than on-time orders.
--
-- On-time orders averaged 61.31 hours between approval and carrier handoff,
-- while delayed orders averaged 127.18 hours.
--
-- Delayed orders required approximately twice the shipping time of
-- on-time orders, suggesting that shipping delays are a major contributor
-- to late deliveries.  


-- ==================================================
-- F4.3: MONTHLY LATE DELIVERY RATE
-- ==================================================

-- Business Question:
-- Which month recorded the highest late delivery rate?  

SELECT
    DATE_FORMAT(order_purchase_timestamp, '%Y-%m') AS month,

    COUNT(*) AS total_orders,

    SUM(
        CASE
            WHEN order_delivered_customer_date >
                 order_estimated_delivery_date
            THEN 1
            ELSE 0
        END
    ) AS late_orders,

    ROUND(
        SUM(
            CASE
                WHEN order_delivered_customer_date >
                     order_estimated_delivery_date
                THEN 1
                ELSE 0
            END
        ) * 100.0 / COUNT(*),
        2
    ) AS late_delivery_rate_pct

FROM valid_orders

WHERE order_purchase_timestamp IS NOT NULL
    AND order_delivered_customer_date IS NOT NULL
    AND order_estimated_delivery_date IS NOT NULL

GROUP BY DATE_FORMAT(order_purchase_timestamp, '%Y-%m')
ORDER BY late_delivery_rate_pct DESC;

-- Key Insight:
-- Among months with meaningful order volume,
-- March 2018 recorded the highest late delivery rate (21.36%),
-- followed by February 2018 (16.00%) and November 2017 (14.31%).
--
-- These periods may indicate capacity constraints,
-- logistics bottlenecks, or seasonal demand pressure
-- affecting delivery performance.
